<h3>Halte deine Personalangaben geheim</h3>
<p>Man wei&#xDF; nie, mit wem man wirklich online spricht. Deshalb gib niemals deinen realen Name, deine Adresse,
    Telefonnummer, Fotos oder Name deiner Schule weiter. Wenn du deine Personalien angibst, bist du gef&#xE4;hrdet und
    riskierst, betrogen und schikaniert zu werden.</p>
<hr>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/safetytips2_n2.png"
        alt="Sch&#xFC;tze deine Privatsph&#xE4;re"
        class="align-right"></p>
<h3>Sch&#xFC;tze deine Privatsph&#xE4;re</h3>
<p>Teile nie deine pers&#xF6;nlichen Daten. Dies gilt ebenfalls f&#xFC;r deine Facebook-, Twitter-, Skype-, Instagram-
    und Snapchat- Angaben. Du weisst nie, wer deine Daten in die H&#xE4;nde kriegt!</p>
<hr>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/safetytips3_n.png"
