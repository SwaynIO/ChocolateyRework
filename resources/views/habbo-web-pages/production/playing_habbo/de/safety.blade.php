<h3>Hab keine Angst davor, etwas laut und deutlich zu sagen.</h3>
<p>Wenn dich jemand bel&#xE4;stigt oder dich in Habbo bedroht, melde das sofort einem Moderator &#xFC;ber den
    Panikknopf.</p>
