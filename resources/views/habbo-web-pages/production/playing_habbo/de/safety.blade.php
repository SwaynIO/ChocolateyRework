<h3>Bleibe beim echten Habbo!</h3>
<p>Webseiten, die dir Taler, Furnis kostenlos anbieten oder sich als ein neues Hotel Habbo oder Staff-Webseiten
    ausgeben, sind Betr&#xFC;gerseiten, die versuchen, dein Passwort zu stehlen. Gib niemals deine Personalangaben
    weiter und lade keine Dateien herunter. Diese k&#xF6;nnen Keyloggers oder Virus sein!</p>
