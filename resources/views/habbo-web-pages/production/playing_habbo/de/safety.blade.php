<h3>Banne die Webcam</h3>
<p>Du hast keine Kontrolle &#xFC;ber deine Fotos und Webcam-Bilder, wenn du sie einmal im Internet geteilt hast, kannst
    du sie nicht zur&#xFC;ckbekommen. Sie k&#xF6;nnen von anderen jederzeit weitergegeben und &#xFC;berall verwendet
    werden, um dich zu schikanieren, zu erpressen oder bedrohen. Bevor du ein Bild postest, frage dich selbst, ob du
    dich wohlf&#xFC;hlen w&#xFC;rdest, wenn andere Leute, die du nicht kennst, deine Bilder sehen k&#xF6;nnen.</p>
<hr>
