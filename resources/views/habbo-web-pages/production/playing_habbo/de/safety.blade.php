<h3>Gebe dem Druck Gleichaltriger nie nach</h3>
<p>Mache nichts, wobei du dich nicht wohlf&#xFC;hlst. Nur weil alle anderen es tun, heisst es nicht, dass du&apos;s auch
    musst!</p>
<hr>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/safetytips4_n.png"
