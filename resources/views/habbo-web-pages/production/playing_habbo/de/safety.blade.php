<h3>Lass deine Kumpels Pixels bleiben</h3>
<p>Triff dich mit niemandem, den du nur vom Internet kennst! Menschen sind nicht immer, was sie vorgeben zu sein. Wenn
    jemand dich auffordert, sich mit ihnen im realen Leben zu treffen, sage &quot;Nein, danke!&quot; und sag einem
    Moderator , deinen Eltern oder einem Erwachsenen deines Vertrauens Bescheid.</p>
