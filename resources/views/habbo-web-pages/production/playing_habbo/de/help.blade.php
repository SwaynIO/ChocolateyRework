<h1>Was tun, wenn du in Habbo auf ein Problem st&#xF6;sst</h1>
<p>Du wirst wahrscheinlich hin und wieder auf den eigenartig ungezogenen Habbo treffen. Aber keine Sorge, es gibt ein
    Gegenmittel f&#xFC;r alles! Auf dieser Seite informieren wir dich &#xFC;ber die vorhandenen Tools zum Melden von
    Usern, wenn du dich in einer kniffligen Situation befindest.</p>
<h2>In einem Raum</h2>
<p>Wenn du dich in einem Raum befindest und ein Habbo verh&#xE4;lt sich unangebracht, klicke auf seinen Avatar und es
    erscheint ein Drop-down Men&#xFC;. Dieses Men&#xFC; gibt dir die M&#xF6;glichkeit den User zu ingorieren, moderieren
    oder in wirklich st&#xF6;renden F&#xE4;llen ihn zu melden.</p>
<h4>Einen Habbo ignorieren</h4>
<p>Wenn <strong>ein Habbo Dinge sagt, die dich unwohl f&#xFC;hlen lassen</strong>, kannst du diesen ignorieren. Das ist
    eine ideale L&#xF6;sung bei Qu&#xE4;lgeistern oder Spammern.</p>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/report_2_de.png"
        alt="Klicke auf einen Avatar zum ignorieren, moderieren oder melden" class="align-right"></p>
<ol>
    <li>Klicke auf seinen Avatar. Es erscheint ein Drop-down Men&#xFC;.</li>
    <li>W&#xE4;hle die <em>Ignorieren</em> Option.</li>
    <li>Du kannst nicht mehr sehen, was dieser Habbo sagt. Falls du ihn sp&#xE4;ter unignorieren willst, klicke wieder
        auf seinen Avatar und w&#xE4;hle <em>Wieder zuh&#xF6;ren</em>.
    </li>
</ol>
<h4>Einen Habbo moderieren</h4>
<p><strong>In deinem eigenen Raum, oder wenn du Raumrechte besitzt</strong>, kannst du entscheiden wer deinen Raum
    besuchen darf und du hast die Macht, User zu stummen, zu kicken oder zu bannen. Dies erm&#xF6;glicht dir, eine
    aktive Rolle bei der Habbo Moderation zu spielen, sowie die Habbo Community sicherer und geniessbarer zu machen.</p>
<p>Lese mehr dar&#xFC;ber im Habbo Helpdesk <a
            href="https://habbohelpde.zendesk.com/entries/22503691-Infos-zu-den-neuen-Raummoderationtools"
            target="_blank">Raummoderationstool</a> und <a
            href="https://habbohelpde.zendesk.com/entries/65914063-Neues-%C3%BCber-den-Raumeinstellungen-Tool-"
            target="_blank">Raumeinstellungen</a>.</p>
<h4>Einen Habbo melden</h4>
<p>Wenn <strong>die Stimmung in einem Raum ein wenig angespannt ist</strong>: Habbos diskutieren &#xFC;ber gef&#xE4;hrliche
    Dinge, wie Treffen im realen Leben, Videochats
    oder Austausch von Kontaktdaten oder jemand wird stark gemobbt&#x2026; &#xFC;berlege dir, diese Person zu melden.
    Niemand mag Petzen, also denk daran dies nur zu benutzen, wenn jemand anderen oder sich absichtlich Schaden zuf&#xFC;gt.
</p>
<ol>
    <li>Klicke auf den Avatar des Habbos der gegen die Regeln verst&#xF6;sst. Es erscheint ein Drop-down Men&#xFC;.</li>
    <li>W&#xE4;hle <em>Melden</em>.</li>
    <li>Markiere die Textzeile die der Moderator sehen soll.</li>
    <li>W&#xE4;hle die beste Beschreibung f&#xFC;r die Situation.</li>
    <li>Schreibe dem Moderator eine kurze Erkl&#xE4;rung was vorgefallen ist.</li>
    <li>Zum Schluss klicke auf <em>Hilferuf senden</em> ein Moderator wird deinen Hilferuf bearbeiten. Wenn du <em>Bel&#xE4;stigung</em>
        w&#xE4;hlst, kann es sein, dass sich ein W&#xE4;chter darum k&#xFC;mmert.
    </li>
</ol>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/help_button_de.png" alt="Hilfe-Schaltfl&#xE4;che"
        class="align-right"></p>
<p><strong>Eine andere Art, einen Habbo zu melden</strong>, ist folgende:</p>
<ol>
    <li>Klicke auf <em>Hilfe</em> in der Ecke rechts oben.</li>
    <li>W&#xE4;hle <em>Einen R&#xFC;pel melden</em>. Es erscheint eine Liste mit all den Habbos die sich im Raum
        befinden.
    </li>
    <li>Klicke auf den Habbo, der gegen die Regeln verst&#xF6;sst.</li>
    <li>Markiere die Textzeile die der Moderator sehen soll.</li>
    <li>W&#xE4;hle die beste Beschreibung f&#xFC;r die Situation.</li>
    <li>Schreibe dem Moderator eine kurze Erkl&#xE4;rung was vorgefallen ist.</li>
    <li>Zum Schluss klicke auf <em>Hilferuf senden</em> ein Moderator wird deinen Hilferuf bearbeiten.</li>
</ol>
