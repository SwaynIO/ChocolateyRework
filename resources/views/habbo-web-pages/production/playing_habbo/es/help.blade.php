<h2>En consola/chat</h2>
<p>Si <strong>est&#xE1;s chateando con alguien en consola y te est&#xE1; haciendo sentir inc&#xF3;modo</strong>:</p>
<ol>
    <li>Haz clic en el bot&#xF3;n <em>RDenunciar</em> que encontrar&#xE1;s debajo de la imagen del Habbo en la ventana
        de chat.
    </li>
    <li>Te preguntar&#xE1;n m&#xE1;s detalles sobre lo ocurrido.</li>
    <li>Tras valorar lo sucedido, un Moderador tomar&#xE1; la acci&#xF3;n adecuada.</li>
</ol>
