<h1>C&#xF3;mo tratar un problema en Habbo</h1>
<p>Quiz&#xE1;s te encuentres de forma ocasional con el mal comportamiento de un Habbo. &#xA1;Nada que temer! &#xA1;La
    ayuda est&#xE1; en camino! En esta p&#xE1;gina te diremos qu&#xE9; herramientas funcionan mejor en cada situaci&#xF3;n.</p>
<h2>En una sala</h2>
<p>Si est&#xE1;s en una sala y otro Habbo te est&#xE1; molestando, puedes hacer clic en su avatar y aparecer&#xE1; un
    men&#xFA; desplegable. Esto te permitir&#xE1; ignorar, moderar o, en casos realmente graves, reportarle.</p>
<h4>Ignorar a un Habbo</h4>
<p>Si <strong>un Habbo est&#xE1; diciendo cosas que te hacen sentir inc&#xF3;modo</strong>, puedes seleccionar la opci&#xF3;n
    &quot;ignorar!. Esta es una soluci&#xF3;n ideal para provocaciones, spam o cuando simplemente quieres que te dejen
    tranquilo y no sabes c&#xF3;mo decirlo.</p>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/report_es.png"
        alt="Haz clic en un avatar para ignorar, moderar o reportar" class="align-right"></p>
<ol>
    <li>Haz clic en su avatar. Se desplegar&#xE1; un men&#xFA;.</li>
    <li>Selecciona la opci&#xF3;n <em>Ignorar</em>.</li>
    <li>No ver&#xE1;s nunca m&#xE1;s lo que ese Habbo dice. Si quieres dejar de ignorarle, haz clic en su avatar de
        nuevo y elige <em>Escuchar</em>.
    </li>
</ol>
<h4>Moderar a un Habbo</h4>
<p><strong>Tanto si est&#xE1;s en una sala tuya, como si es de un amgio pero tienes derechos sobre ella</strong>, puedes
    decidir qui&#xE9;n puede entrar a visitarla. Adem&#xE1;s, tambi&#xE9;n tienes la posibilidad de mutear, expulsar o
    bannear a otros usuarios. Esto te permite ser parte activa de la moderaci&#xF3;n general de Habbo y contribuir con
    ello a crear una comunidad m&#xE1;s segura y divertida.</p>
<p>Puedes obtener m&#xE1;s informaci&#xF3;n en nuestra secci&#xF3;n de Atenci&#xF3;n al Usuario en los apartados <a
            href="https://help.habbo.es/entries/22494533-Modera-tus-Salas-de-Habbo" target="_blank">Herramientas de
        Moderaci&#xF3;n de Sala</a> y <a
            href="https://help.habbo.es/entries/69094616-Todo-sobre-los-Ajustes-de-las-Salas" target="_blank">Ajustes de
        Sala</a>.</p>
<h4>Reportar a un Habbo</h4>
<p>Si <strong>en tu sala se empiezan a dar las siguientes situaciones</strong>: los Habbos est&#xE1;n hablando de
    encontrarse en la vida real, quieren una videollamada, algunos est&#xE1;n intercambiando informaci&#xF3;n personal
    de contacto o alguien est&#xE1; sufriendo bullying, puedes considerar reportar a esa persona. A nadie le gustan los
    soplones, as&#xED; que recuerda usarlo s&#xF3;lo cuando se est&#xE1; da&#xF1;ando a otros intencionadamente o a s&#xED;
    mismos.</p>
<ol>
    <li>Haz clic en el avatar del Habbo que crea problemas. Se desplegar&#xE1; un men&#xFA;.</li>
    <li>Selecciona <em>Reportar</em>.</li>
    <li>Marca las l&#xED;neas de chat que los moderadores deber&#xED;an ver.</li>
    <li>Elige la mejor descripci&#xF3;n de la situaci&#xF3;n.</li>
    <li>Dile al moderador lo que ha ocurrido.</li>
    <li>Finalmente haz clic en <em>Pedir Ayuda</em>. Si eliges <em>Acosos</em> un Guardi&#xE1;n podr&#xE1; intervenir.
    </li>
</ol>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/help_button_es.png" alt="Bot&#xF3;n de Ayuda"
        class="align-right">
</p>
<p>Otra manera de hacer lo mismo es &#xE9;sta:</p>
<ol>
    <li>Haz clic en <em>Ayuda</em> en la esquina superior derecha.</li>
    <li>Selecciona <em>Reportar un acosador</em>. Ver&#xE1;s una lista con todos los Habbos en la sala.</li>
    <li>Haz clic sobre el Habbo que se est&#xE1; comportando mal.</li>
    <li>Marca las l&#xED;neas de chat que los moderadores deber&#xED;an ver.</li>
    <li>Elige la mejor descripci&#xF3;n de la situaci&#xF3;n.</li>
    <li>Dile al moderador lo que ha ocurrido.</li>
    <li>Manda el reporte y el moderador intentar&#xE1; resolver el asunto.</li>
</ol>
