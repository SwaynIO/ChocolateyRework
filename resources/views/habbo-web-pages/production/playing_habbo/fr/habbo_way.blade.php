<h4>Jouer &amp; Cr&#xE9;er des jeux</h4>
<p>Joue avec tes amis ou cr&#xE9;e tes propres jeux pour devenir populaire! Si un jeu est populaire, tout le monde
    voudra rentrer dans ton appart et se joindre &#xE0; toi pour passer un bon moment.</p>
<h4>Se faire des amis</h4>
<p>Amuse-toi, sors et tu trouveras peut-&#xEA;tre pixel &#xE0; ton pied&#x2026;</p>
