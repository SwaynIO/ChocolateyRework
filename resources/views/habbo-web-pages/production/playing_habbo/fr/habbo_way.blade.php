<h2>NE PAS FAIRE&#x2026;</h2>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/habboway_2b.png" alt="Ne pas troller"
        class="align-right"></p>
<h4>TrollER</h4>
<p>Personne n&apos;aime les trolls, m&#xEA;me pas leur m&#xE8;re! Le harc&#xE8;lement sur Habbo n&apos;est pas tol&#xE9;r&#xE9;.</p>
<h4>Voler ou scripter</h4>
<p>Cr&#xE9;e, mais ne copie pas! Personne n&apos;aime les tricheurs. Voler ne te rend pas plus riche, &#xE7;a fait de
    toi un criminel.</p>
<h4>Tricher ou arnaquer</h4>
<p>Les tricheurs ne prosp&#xE8;rent jamais; ils g&#xE2;chent juste l&apos;exp&#xE9;rience des autres Habbos.</p>
<p><img src="{{$chocolatey->hotelUrl}}habbo-web/assets/web-images/habboway_7b.png"
        alt="Ne pas vendre contre de l&apos;argent r&#xE9;el" class="align-right"></p>
<h4>Vendre contre de l&apos;argent r&#xE9;el</h4>
<p>Il ne faut jamais vendre des mobis, des comptes, des mots de passe ou n&apos;importe quelle autre chose du jeu contre
    de l&apos;argent r&#xE9;el. Il est probable que tu perdes le tout dans un endroit pas tr&#xE8;s s&#xFB;r et tu
    jetterais &#xE0; la poubelle tous les efforts que tu as faits pour collectionner toutes ces choses!</p>
<h4>Faire ou accepter des paris</h4>
<p>Tu pourrais avoir des ennuis en utilisant des mobis qui peuvent rendre un jeu al&#xE9;atoire, avec la possibilit&#xE9;
    de faire des paris. Laisser le hasard faire? N&apos;y pense m&#xEA;me pas.</p>
