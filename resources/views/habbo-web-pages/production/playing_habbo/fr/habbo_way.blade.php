<h1>Habbo Attitude</h1>
<p>La Habbo Attitude est comme un code de conduite, un guide qui enseigne le comportement que les Habbos doivent avoir
    dans l&apos;H&#xF4;tel. Habbo est bien plus fun quand on respecte les r&#xE8;gles!</p>
<h2>Que faire&#x2026;</h2>
